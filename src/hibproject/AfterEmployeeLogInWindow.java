    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Configuration conf = new Configuration();
        Configuration c1 = conf.configure();
        SessionFactory sf;
        sf = c1.buildSessionFactory();
        Session session = sf.openSession();
        Transaction tx = session.beginTransaction();
        
        Criteria crit = session.createCriteria(FIR.class);
        crit.addOrder(Order.desc("FIRNo"));
        crit.setMaxResults(1);
        List<FIR> list = crit.list();
        if(list.isEmpty()) { 
        int firNoNew = 1;
        String firName = jTextField1.getText();
        String firAdd = jTextField3.getText();
        String firAadharNo = jTextField2.getText();
        String firContactNo = jTextField5.getText();
        String firStatment = jTextArea1.getText();
        Date date = new Date();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/mm/yyyy hh:mm:ss");
        String firtime = sdf.format(date);
        FIR fir1 = new FIR(firNoNew, firName, firAdd, firAadharNo, firContactNo, firtime , firStatment);
        session.save(fir1);
        tx.commit();
        session.close();
        JOptionPane.showMessageDialog(this, "Success!!!! Your FIR number is :"+ firNoNew);
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField5.setText("");
        jTextArea1.setText("");
        }
        else {
            for(FIR fir : list) {
            int firNoNew = fir.getFIRNo()+1;
            
        String firName = jTextField1.getText();
        String firAdd = jTextField3.getText();
        String firAadharNo = jTextField2.getText();
        String firContactNo = jTextField5.getText();
        String firStatment = jTextArea1.getText();
        Date date = new Date();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/mm/yyyy hh:mm:ss");
        String firtime = sdf.format(date);
        FIR fir1 = new FIR(firNoNew, firName, firAdd, firAadharNo, firContactNo, firtime , firStatment);
        session.save(fir1);
        tx.commit();
        session.close();
        JOptionPane.showMessageDialog(this, "Success!!!! Your FIR number is :"+ firNoNew);
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField5.setText("");
        jTextArea1.setText("");
            }
        }
    
    }//GEN-LAST:event_jButton1ActionPerformed

